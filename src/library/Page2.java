    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String un = jTextField1.getText();
        String pass = jPasswordField1.getText();
        int userId= Integer.parseInt(un),flag=0;
        if(userId==l.user_id)
	{
        	if(pass.equals(l.password))
		{
                       flag=1;
                       JOptionPane.showMessageDialog(j,"Librarian Login");
                       this.dispose();
                       Page4 p4=new Page4(users,books,l,usr,k);
                       p4.setVisible(true);
		}                
	}
	else if(matchUser(userId)>=0)
	{
        	int k;
		k=matchUser(userId);
		if(pass.equals(users.get(k).password))
		{
			flag=1;
			JOptionPane.showMessageDialog(j,"User Login");
                        this.dispose();
                        Page3 p3=new Page3(users,books,l,usr,k);
                        p3.setVisible(true);
		}
		else
		JOptionPane.showMessageDialog(j,"Password Mismatched");
	}
	if(flag==0)
        {
		JOptionPane.showMessageDialog(j,"Invalid");
	}		             
    }//GEN-LAST:event_jButton1ActionPerformed

